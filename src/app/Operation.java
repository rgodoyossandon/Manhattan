    private void acceptbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptbuttonActionPerformed
      if(buymoney.isSelected()==false && sellmoney.isSelected()==false && getmoney.isSelected()==false)
    {
        JOptionPane.showMessageDialog(null,"No selecciono ninguna opciÃ³n");
    }else if(buymoney.isSelected()==true && sellmoney.isSelected()==false && getmoney.isSelected()==false)
        
    {
        Bills bill = new Bills();
        bill.setVisible(true);
        
        dispose();
        Bills.totallabel.setText(Double.toString(usachange * Admin.usatotal));

    }else if(buymoney.isSelected()==false && sellmoney.isSelected()==true && getmoney.isSelected()==false)
        
    {
        Bills bill = new Bills();
        bill.setVisible(true);
        
        dispose();
        Bills.totallabel.setText(Double.toString(usachange * Admin.usatotal));
    }else if(buymoney.isSelected()==false && sellmoney.isSelected()==false && getmoney.isSelected()==true)
        
    {
        Bills bill = new Bills();
        bill.setVisible(true);
        
        dispose();
        Bills.totallabel.setText(Double.toString(usachange * Admin.usatotal));

    }//GEN-LAST:event_acceptbuttonActionPerformed
    }
